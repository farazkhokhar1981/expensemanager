package com.hrptech.expensemanager.beans;

public class LockBeans {

    private String lock_password;
    private String lock_question;
    private String lock_answer;
    private String lock_password_hint;
    private String lock_contact;


    public String getLock_password() {
        return lock_password;
    }

    public void setLock_password(String lock_password) {
        this.lock_password = lock_password;
    }

    public String getLock_question() {
        return lock_question;
    }

    public void setLock_question(String lock_question) {
        this.lock_question = lock_question;
    }

    public String getLock_answer() {
        return lock_answer;
    }

    public void setLock_answer(String lock_answer) {
        this.lock_answer = lock_answer;
    }

    public String getLock_password_hint() {
        return lock_password_hint;
    }

    public void setLock_password_hint(String lock_password_hint) {
        this.lock_password_hint = lock_password_hint;
    }

    public String getLock_contact() {
        return lock_contact;
    }

    public void setLock_contact(String lock_contact) {
        this.lock_contact = lock_contact;
    }
}
